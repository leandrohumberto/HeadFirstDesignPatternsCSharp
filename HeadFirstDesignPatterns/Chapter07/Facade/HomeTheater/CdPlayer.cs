using System;

namespace Chapter07.HomeTheater
{
    class CdPlayer
    {
        public void On()
        {
            Console.WriteLine("Top-O-Line CD Player on");
        }

        public void Off()
        {
            Console.WriteLine("Top-O-Line CD Player off");
        }

        public void Eject()
        {
            Console.WriteLine("Top-O-Line CD Player eject");
        }

        public void Pause()
        {
            Console.WriteLine("Top-O-Line CD Player pause");
        }

        public void Play()
        {
            Console.WriteLine("Top-O-Line CD Player play");
        }

        public void Stop()
        {
            Console.WriteLine("Top-O-Line CD Player stop");
        }
    }
}

using System;

namespace Chapter07.HomeTheater
{
    class PopcornPopper
    {
        public void On()
        {
            Console.WriteLine("Top-O-Line Popcorn Popper on");
        }

        public void Off()
        {
            Console.WriteLine("Top-O-Line Popcorn Popper off");
        }

        public void Pop()
        {
            Console.WriteLine("Top-O-Line Popcorn Popper pop");
        }
    }
}
